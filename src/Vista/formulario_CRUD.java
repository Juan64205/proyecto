    private void BTN_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_RegistrarActionPerformed
        if (this.txt_codigoProducto.getText().length() == 0 || this.txt_nombreProducto.getText().length() == 0 || this.txt_registroProducto.getText().length() == 0) {
            this.lbl_error.setText("Â¡Todos los campos son requeridos!");
        } else {
            Sentencias s = new Sentencias();
            s.setId(this.lbl_id.getText());
            s.setId(this.txt_codigoProducto.getText());
            s.setId(this.txt_nombreProducto.getText());
            s.setId(this.txt_registroProducto.getText());
            
            if (Funciones.isRegister(s)) {
                Funciones.setListar("");
                JOptionPane.showMessageDialog(this, "Nuevo producto registrado con exito", "informaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Error al intentar registrar el producto", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BTN_RegistrarActionPerformed

